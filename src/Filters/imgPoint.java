package Filters;

public class imgPoint {
    private int row, col;
    public imgPoint(int r, int c){
        this.row = r;
        this.col = c;
    }

    public int getRow() {
        return row;
    }
    public void setRow(int row) {
        this.row = row;
    }
    public int getCol() {
        return col;
    }
    public void setCol(int col) {
        this.col = col;
    }
}
